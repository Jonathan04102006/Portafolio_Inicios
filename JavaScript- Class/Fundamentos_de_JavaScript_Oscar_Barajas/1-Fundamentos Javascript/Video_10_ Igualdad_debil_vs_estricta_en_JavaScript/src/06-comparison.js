// Operadores de comparacion en JS: Igualdad y Desigualdad

// Igualdad Débil (==)

console.log(5 == '5'); // true
console.log(true == 1); // true
console.log(false == 0); // true
console.log(null == undefined); // true

// Desigualdad débil (!=)

console.log(5 != '5'); // false

// Igualdad Estricta

console.log( 5 === 5); // true
console.log( 5 === '5'); // false

// Desigualdad Estricta

console.log(5 !== '5');
console.log(5 !== 5);
